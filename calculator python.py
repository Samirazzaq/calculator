#Calculator
val1=int(input("Enter value no1"));
val2=int(input("Enter value no2"));
operator=input("Enter operator");

if operator == ("+"):
    val=val1+val2
    print(val,"Answer")
elif operator == ("-"):
    val=val1-val2
    print(val,"Answer")
elif operator == ("*"):
    val=val1*val2
    print(val,"Answer")
elif operator == ("/"):
    val=val1/val2
    print(val,"Answer")
else:
    print("Incorrect Operator")


